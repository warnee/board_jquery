function endsWith(sourceText, pattern) {
    var returnValue = false;
    if (sourceText.length >= pattern.length) {
        if (sourceText.substr((sourceText.length - pattern.length), sourceText.length) == pattern) {
            returnValue = true;
        }
    }
    return returnValue;
}

function startsWith(sourceText, pattern) {
    var returnValue = false;
    if (sourceText.length >= pattern.length) {
        if (sourceText.substr(0, pattern.length) == pattern) {
            returnValue = true;
        }
    }
    return returnValue;
}

function contains(sourceText, pattern) {
    var returnValue = false;
    if (new RegExp(pattern, "g").test(sourceText)) {
        returnValue = true;
    }
    return returnValue;
}

function setCookie(c_name, value, expiredays) {
    var exdate = new Date();
    exdate.setDate(exdate.getDate() + expiredays);
    document.cookie = c_name + "=" + escape(value) + ((expiredays == null) ? "" : ";expires=" + exdate.toGMTString()) + ";path=/";
}

function getCookie(c_name) {
    if (document.cookie.length > 0) {
        c_start = document.cookie.indexOf(c_name + "=");
        if (c_start != -1) {
            c_start = c_start + c_name.length + 1;
            c_end = document.cookie.indexOf(";", c_start);
            if (c_end == -1) c_end = document.cookie.length;
            return unescape(document.cookie.substring(c_start, c_end));
        }
    }
    return '';
}

function getBrowserEnginVersion(ver) {
    var rVersion = -1;
    var ua = navigator.userAgent;
    var re = null;
    if (ver == "MSIE") {
        re = new RegExp("MSIE ([0-9]{1,}[\.0-9]{0,})");
    }
    else {
        re = new RegExp(ver + "/([0-9]{1,}[\.0-9]{0,})");
    }
    if (re.exec(ua) != null) {
        rVersion = parseFloat(RegExp.$1);
    }
    return rVersion;
}


